package com.travelportal.vm;

import java.util.List;


public class BatchMarkupVM {
	
	public List<Integer> supplier;
	public List<Integer> agent;
	public String selected;
	public Double flat;
	public Double percent;
	
	
	
	
	public List<Integer> getSupplier() {
		return supplier;
	}
	public void setSupplier(List<Integer> supplier) {
		this.supplier = supplier;
	}
	public List<Integer> getAgent() {
		return agent;
	}
	public void setAgent(List<Integer> agent) {
		this.agent = agent;
	}
	public String getSelected() {
		return selected;
	}
	public void setSelected(String selected) {
		this.selected = selected;
	}
	public Double getFlat() {
		return flat;
	}
	public void setFlat(Double flat) {
		this.flat = flat;
	}
	public Double getPercent() {
		return percent;
	}
	public void setPercent(Double percent) {
		this.percent = percent;
	}
	
	
	
	
	
	
}
